    /**
     * 
     * @param objeto 
     */
    private static void escribeFichero (Object objeto) {
        boolean abrir;
        Archivo_Objetos archivo=new Archivo_Objetos();
        abrir=archivo.CrearArchivoProveedores();
        Proveedor nuevo=new Proveedor();
        nuevo=(Proveedor) objeto;
        if(abrir==false)
            archivo.EscribirArchivoProveedores(nuevo);
        else
            archivo.AñadirArchivoProveedores(nuevo);
    }


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (!jTextField1.getText().equals("") && !jTextField2.getText().equals("") && !jTextField3.getText().equals("") && !jTextField4.getText().equals("") && !jTextField5.getText().equals("")) {

            /**
            * Encerramos en un bloque try - catch los accesos por teclado.
            */
            try {

                /**
                * Solicitamos los datos de los atributos del objeto producto.
                * Conforme los solicitamos, con su metodo set respectivo, almacenamos el
                * atributo.
                */
                proveedor.setCodigo(Integer.parseInt(jTextField1.getText()));
                proveedor.setSaldo(Float.parseFloat(jTextField2.getText()));
                proveedor.setNombre(jTextField3.getText());
                proveedor.setDireccion(jTextField4.getText());
                proveedor.setPoblacion(jComboBox1.getSelectedItem().toString());
                proveedor.setNif(jTextField5.getText());
            }

            /**
            * Capturamos las excepciones. Mostramos mensaje por salida.
            */
            catch(Exception e){}

            /**
             * Grabamos el objeto en un archivo.
             */
            escribeFichero (proveedor);

            /**
             * Mostramos mensaje de confirmacion.
             */
            JOptionPane.showMessageDialog(null,"Los datos se han grabado.");
        }
        else{
            /**
             * Mostramos mensaje de error en la entrada de datos.
             */
            JOptionPane.showMessageDialog(null,"Solo son validos valores tipo int entero en codigo, tipo float en saldo y los demas campos tipo string.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


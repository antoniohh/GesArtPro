    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            
        if (!jTextField1.getText().equals("") 
                && !jTextField2.getText().equals("") 
                && !jTextField3.getText().equals("") 
                && !jTextField4.getText().equals("") 
                && !jTextField5.getText().equals("")) {

            /**
            * Encerramos en un bloque try - catch los accesos por teclado.
            */      
            try {

                /**
                * Solicitamos los datos de los atributos del objeto producto.
                * Conforme los solicitamos, con su metodo set respectivo, 
                * almacenamos el atributo.
                */
                articulo.setC_barra(jTextField1.getText());
                articulo.setDenominacion(jTextField2.getText());
                articulo.setBeneficio(Integer.parseInt(jTextField3.getText()));
                precioCompra=Double.parseDouble(jTextField5.getText());
                unidadesCompradas=Integer.parseInt(jTextField4.getText());
                articulo.setTipo(jComboBox1.getSelectedItem().toString());
            }

            /**
            * Capturamos las excepciones. Mostramos mensaje por salida.
            */
            catch(Exception e){}
            
            // Asignamos con la ecuacion el nuevo precio de compra
            articulo.Comprar(precioCompra,unidadesCompradas);

            // Grabamos el objeto en un archivo.
            escribeFichero (articulo);

            // Mostramos mensaje de confirmacion.
            JOptionPane.showMessageDialog(null,"Los datos se han grabado.");
        }
        
        else{
            // Mostramos mensaje de error en la entrada de datos.
            JOptionPane.showMessageDialog(null,"Solo son validos valores tipo "
                    + "int entero y tipo double en el precio.");
        }

    }//GEN-LAST:event_jButton2ActionPerformed


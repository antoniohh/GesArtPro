    /**
     * 
     * @param objeto 
     */
    private static void escribeFichero (Object objeto) {
        boolean abrir;
        Archivo_Objetos archivo=new Archivo_Objetos();
        abrir=archivo.CrearArchivoArticulos();
        Articulo nuevo=new Articulo();
        nuevo=(Articulo) objeto;
        if(abrir==false)
            archivo.EscribirArchivoArticulos(nuevo);
        else
            archivo.AñadirArchivoArticulos(nuevo);
    }

